const mongoose = require('mongoose');

const dgSchema = new mongoose.Schema(
    {
        nev:{
            type: String,
            require: true,
        },
        fajta: {
            type: String,
            required: true
        },
        ar: {
            type: Number
        },
        kepek: [{
            type: String  
        }]
}, {
    timestamps: true
});
    

const DgModel = mongoose.model('Dg', dgSchema);

module.exports = DgModel;